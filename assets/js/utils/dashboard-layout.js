/**
 * Dashboard Layout Component Injector
 * Injects sidebar and mobile header into dashboard pages
 * 
 * Usage:
 * <div id="dashboard-layout-container"></div>
 * <script src="assets/js/utils/dashboard-layout.js"></script>
 * <script>
 *   DashboardLayout.inject('dashboard-layout-container');
 * </script>
 */

